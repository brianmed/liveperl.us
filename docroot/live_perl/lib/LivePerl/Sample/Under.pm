package LivePerl::Sample::Under;
use Mojo::Base 'LivePerl::Sample';

has doc_url => 'http://mojolicio.us/perldoc/Mojolicious/Lite#Under';
has description => 'Authentication and code shared between multiple routes can be realized easily with bridge routes generated by the "under" statement. All following routes are only evaluated if the callback returned a true value.';
has title => 'Under';

1;
__DATA__
use Mojolicious::Lite;

get '/' => sub {
    my $self = shift;
 
    # We're dynamic 'yo
    my $project_name = "LivePerl";
 
    my $person = $self->session("person");
    
    $self->render(template => "slash", project_name => $project_name, person => $person);
};


# Authenticate based on name parameter
under sub {
  my $self = shift;

  # Authenticated
  my $person = $self->param('person') || $self->session("person") || '';
  return 1 if $person eq 'Bender';

  # Not authenticated
  $self->render('denied', project_name => "LivePerl", person => $person);
  return undef;
};

# Only reached when authenticated 
any '/auth' => sub {
    my $self = shift;
 
    # We're dynamic 'yo
    my $project_name = "LivePerl";
 
    my $person = $self->param("person") || $self->session("person");
    
    $self->render(template => "auth", project_name => $project_name, person => $person);
};

push(@{app->static->paths}, '/src');

app->start;
 
__DATA__
 
